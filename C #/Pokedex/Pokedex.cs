        private void PokedexDoc_PrintPage(object sender, System.Drawing.Printing.PrintPageEventArgs e)
        {
            /*Creates a Brush Bolour that Matches the Pokémon Background
              and Applies a Rectangle to the Background*/ 
            SolidBrush rectangle = new SolidBrush(this.BackColor);
            e.Graphics.FillRectangle(rectangle, 50, 100, 725, 450);

            Font f1 = new Font("Arial", 22, FontStyle.Bold);
            Font f2 = new Font("Arial", 18, FontStyle.Bold);
            Font f3 = new Font("Arial", 16, FontStyle.Regular);

            e.Graphics.DrawString("Pokédex Generation I",f1,Brushes.Black,250,50);

            e.Graphics.DrawString("Number: ", f2, Brushes.Black, 110, 165);
            e.Graphics.DrawString(number.Text, f3, Brushes.Black, 220, 167);

            e.Graphics.DrawString("Name: ", f2, Brushes.Black, 110, 195);
            e.Graphics.DrawString(name.Text, f3, Brushes.Black, 195, 197);

            e.Graphics.DrawString("Species: ", f2, Brushes.Black, 110, 225);
            e.Graphics.DrawString(species.Text, f3, Brushes.Black, 220, 227); 

            e.Graphics.DrawString("Egg Group: ", f2, Brushes.Black, 110, 255);
            e.Graphics.DrawString(egg_group.Text, f3, Brushes.Black, 255, 257);

            e.Graphics.DrawString("Native to: ", f2, Brushes.Black, 110, 285);          
            e.Graphics.DrawString(native_to.Text, f3, Brushes.Black, 235, 287);

            e.Graphics.DrawString("Evolution: ", f2, Brushes.Black, 110, 395);     
            e.Graphics.DrawString(name_evolution.Text, f3, Brushes.Black, 245, 397);

            e.Graphics.DrawString("Evolution Type: ", f2, Brushes.Black, 110, 425);
            e.Graphics.DrawString(type_evolution.Text, f3, Brushes.Black, 305, 427);
            


            e.Graphics.DrawImage(pic_preview.Image, 520, 110, 200, 200);
