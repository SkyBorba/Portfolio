        private void btn_previous_Click(object sender, EventArgs e)
        {
            //Button to go Back
            index--;
            if (index < 0)
            {
                MessageBox.Show("Beguinning", "Warning!", MessageBoxButtons.OK, MessageBoxIcon.Error);
                index = 0;
            }

            Color_Change();
            show();
        }

        private void btn_next_Click(object sender, EventArgs e)
        {
            //Button to go Foward
            index++;
            if (index > 99)
            {
                MessageBox.Show("Ending", "Warning!", MessageBoxButtons.OK, MessageBoxIcon.Error);
                index = 99;
            }

            Color_Change();
            show();
        }

